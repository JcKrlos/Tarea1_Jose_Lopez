public class Iniciales {
    public static void main(String[] args) {
        imprimirJ();
        imprimirC();
        imprimirL();
        imprimirR();
    }

    public static void imprimirJ() {
        System.out.println("   ***");
        System.out.println("     *");
        System.out.println("     *");
        System.out.println(" *   *");
        System.out.println("  *** ");
        System.out.println(" ");
    }

    public static void imprimirC() {
        System.out.println("  ****");
        System.out.println(" *    ");
        System.out.println(" *    ");
        System.out.println(" *    ");
        System.out.println("  ****");
        System.out.println(" ");
    }

    public static void imprimirL() {
        System.out.println(" *    ");
        System.out.println(" *    ");
        System.out.println(" *    ");
        System.out.println(" *    ");
        System.out.println(" *****");
        System.out.println(" ");
    }

    public static void imprimirR() {
        System.out.println(" **** ");
        System.out.println(" *   *");
        System.out.println(" **** ");
        System.out.println(" *  * ");
        System.out.println(" *   *");
    }
}